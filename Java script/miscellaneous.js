/* Difference between for, for-in and for-of loops

for: A versatile loop for running a block of code a specific number of times.

for...in: Best for iterating over object properties (not recommended for arrays).

for...of: Ideal for iterating over values in arrays, strings, maps, sets, and other iterable objects.

forEach: Array method for iterating over elements with no option to break/continue.

while: Runs a block of code while a condition is true.

do...while: Similar to while, but guarantees at least one execution.

*/